package week1exercises;

public class Swapping {
	
	public static void main(String[] args) {
		 //Initialize the Data type , variables and values - 
		int a=2;
		int b=3;
		System.out.println("Before swapping \t");
		System.out.println("A value: \t"+a);
		System.out.println("B value: \t"+b);
		a=a+b; // a=5
		b=a-b; // b=5-3 = 2
		a=a-b; // b=5-2=3
		
		System.out.println("After swapping \t");
		System.out.println("A value: \t"+a);
		System.out.println("B value: \t"+b); 
		 
	}
		
		

	}


